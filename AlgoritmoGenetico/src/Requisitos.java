
public class Requisitos {
	
	private double Custo;
	private int Risco;
	private int importanciaMedia;
	public double getCusto() {
		return Custo;
	}
	public void setCusto(double d) {
		Custo = d;
	}
	public int getRisco() {
		return Risco;
	}
	public void setRisco(int risco) {
		Risco = risco;
	}
	public int getImportanciaMedia() {
		return importanciaMedia;
	}
	public void setImportanciaMedia(int importanciaMedia) {
		this.importanciaMedia = importanciaMedia;
	}

}
